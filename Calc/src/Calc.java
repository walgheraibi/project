
public class Calc {
public Calc()
{
}
public  double modulus(double firstInput, double secontInput) {
	return firstInput % secontInput;
}

public  double division(double firstInput, double secontInput) {
	return firstInput/secontInput;
}

public double multiplication(double firstInput, double secontInput) {
	return firstInput*secontInput;
}

public  double subtraction(double firstInput, double secontInput) {
	return firstInput-secontInput;
}

public   double addition(double firstInput, double secontInput) {
	return firstInput+secontInput;
}
}
